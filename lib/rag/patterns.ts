// ============================================================================
// 共享文本模式
// ----------------------------------------------------------------------------
// 法规条文常用中文数字（如"九十平方米""一点零个""百分之三十"），因此数值
// 检测需同时兼容阿拉伯数字与中文数字，避免把"有明确数值"的条文误判为无数值。
// ============================================================================

const CHINESE_NUM = "零一二三四五六七八九十百千万点";

/** 数值 + 计量单位（阿拉伯或中文数字）。每次调用返回新实例，避免全局 lastIndex 副作用。 */
export function numericRe(): RegExp {
  return new RegExp(
    `(?:\\d+(?:\\.\\d+)?|[${CHINESE_NUM}]+)\\s*(?:%|％|平方米|米|个|户|辆)`,
    "g"
  );
}

/** 文本是否包含明确的量化标准（数值 + 单位）。 */
export function containsNumeric(text: string): boolean {
  return numericRe().test(text);
}

/** 用地代码 / 用地类型识别。 */
export const LAND_USE_RE =
  /[A-Za-z]\d{1,2}|[一二三四]类居住用地|商业用地|商务金融用地|工业用地|公共服务设施用地/g;

// ============================================================================
// 查询短语抽取（检索相关度）
// ----------------------------------------------------------------------------
// BM25 把问题切成单字 + 全部相邻 2-gram，导致"用地""服务"等高频碎片淹没真正的
// 关键名词短语。queryPhrases 抽出问题中的"强名词短语"（领域词、代码、条款号、
// 去虚词后的最大中文片段），供 rerank 做"子串命中 + 按短语长度加权"的精确加分：
// 越长越具体的短语命中，得分越高，从而把字面包含关键短语的 chunk 顶上来。
// ============================================================================

/** 切分短语用的虚词/疑问词/比较词（仅用于查询侧解析）。按长度降序应用。 */
const PHRASE_BREAKERS = [
  "是什么", "什么", "多少", "如何", "怎样", "怎么", "哪些", "哪个", "几个", "请问",
  "不低于", "不高于", "不应", "不得", "低于", "高于", "大于", "小于", "达到",
  "以上", "以下", "之间", "区别", "定义",
  "的", "是", "在", "和", "与", "及", "或", "为", "其", "该", "以", "对",
  "了", "也", "并", "有", "吗", "呢", "应", "宜", "可", "请",
].sort((a, b) => b.length - a.length);

/** 领域强词：从长串中抽出可匹配的子短语（通用规划/国土术语，非按文件名硬编码）。 */
const DOMAIN_TERMS = [
  "容积率", "建筑密度", "绿地率", "建筑高度", "建筑面积", "用地面积", "用地规模",
  "建筑规模", "千人指标", "服务规模", "一般规模", "服务半径", "服务内容",
  "配置指标", "配建指标", "控制指标", "配置要求", "管控要求", "停车位", "配建",
  "居住用地", "二类居住用地", "一类居住用地", "商业用地", "商务金融用地",
  "工业用地", "公共服务设施", "日照", "间距", "退线", "道路红线", "红线宽度",
  "用地分类", "类别代码", "用地代码", "图书展览用地", "物业服务用房",
  "社区卫生服务中心", "幼儿园", "养老", "停车场", "比例尺", "格式要求",
];

export interface QueryPhrase {
  text: string;
  len: number;
}

/** 从问题抽出强名词短语（去重，带长度权重）。 */
export function queryPhrases(question: string): QueryPhrase[] {
  const out = new Map<string, number>();
  const add = (s: string) => {
    const t = s.trim();
    if (t.length >= 2) out.set(t, t.length);
  };

  // 1) 领域强词（直接子串命中）
  for (const term of DOMAIN_TERMS) if (question.includes(term)) add(term);

  // 2) 代码 / 条款号
  for (const m of question.matchAll(/[A-Za-z]\d{1,3}/g))
    out.set(m[0].toUpperCase(), m[0].length);
  for (const m of question.matchAll(/第[一二三四五六七八九十百零0-9]+条/g)) add(m[0]);

  // 3) 去虚词后的最大中文片段
  let s = question;
  for (const b of PHRASE_BREAKERS) s = s.split(b).join(" ");
  s = s.replace(/[^一-龥]+/g, " ");
  for (const run of s.split(/\s+/)) add(run);

  return [...out.entries()].map(([text, len]) => ({ text, len }));
}
